CREATE TABLE call_center (
    id VARCHAR(255),
    customer_name VARCHAR(255),
    sentiment VARCHAR(255),
    csat_score NUMERIC,
    call_timestamp DATE,
    reason VARCHAR(255),
    city VARCHAR(255),
    state VARCHAR(255),
    channel VARCHAR(255),
    response_time VARCHAR(255),
    call_duration_in_minutes NUMERIC,
    call_center VARCHAR(255)
);